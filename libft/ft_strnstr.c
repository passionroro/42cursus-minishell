#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, t_size len)
{
	t_size	i;
	t_size	j;

	i = 0;
	j = 0;
	if (*needle == '\0' || needle == NULL)
		return ((char *)haystack);
	while (haystack[i] && i < len)
	{
		j = 0;
		while (haystack[i + j] == needle[j] && i + j < len)
		{
			if (needle[j + 1] == '\0')
				return ((char *)haystack + i);
			j++;
		}
		i++;
	}
	return (0);
}
