#include "libft.h"

void	ft_bzero(void *s, t_size n)
{
	unsigned char	*ptr;
	t_size			i;

	i = 0;
	ptr = s;
	while (i < n)
	{
		ptr[i] = '\0';
		i++;
	}
}
